package com.itt.tds.core;

public class TDSEnumException extends Exception {

	public TDSEnumException() {

		super();
	}

	public TDSEnumException(String message) {

		super(message);
	}

	public TDSEnumException(String message, Throwable cause) {

		super(message, cause);
	}

	public TDSEnumException(Throwable cause) {

		super(cause);
	}
}